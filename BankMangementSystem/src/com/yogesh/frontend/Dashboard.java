    private void btnCloseAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCloseAccountActionPerformed
        // TODO add your handling code here:

        final int BALANCE_REQUIRE_TO_CLOSE = 1 ;
        int choice = JOptionPane.showConfirmDialog(null, "Are You Sure for Close Account ?" , "Confirmation", JOptionPane.YES_NO_OPTION);

        if(choice == JOptionPane.YES_OPTION)
        {
            String accountNo = txtCloseAccountNo.getText().trim();
            ResultSet resultSet = new AccountAvailableClass().accountAvailable(accountNo);

            if(resultSet != null)
            {
                try {
                    int balanceAvailable = Integer.parseInt(resultSet.getString(16));

                    if(balanceAvailable < BALANCE_REQUIRE_TO_CLOSE ){
                        new CloseAccount(accountNo);
                        JOptionPane.showMessageDialog(null, "Account Close Successfully .. !!!");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "PLEASE WITHDRAW YOUR BALANCE TO CLOSE ACCOUNT ... !!!\n" + resultSet.getString(16) );
                    }

                } catch (SQLException ex) {
                    Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Account Not Found .... !!!!");
            }

        }else{
            pnlCloseAccountPanel.setVisible(false);
        }
    }//GEN-LAST:event_btnCloseAccountActionPerformed

